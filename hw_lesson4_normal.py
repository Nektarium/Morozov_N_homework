# Задание-3:
# Напишите скрипт, заполняющий указанный файл (самостоятельно задайте имя файла)
# произвольными целыми цифрами, в результате в файле должно быть
# 2500-значное произвольное число.
# Найдите и выведите самую длинную последовательность одинаковых цифр
# в вышезаполненном файле.

random_list = [random.randint(0, 9) for _ in range(2500)]
number = int(''.join(str(i) for i in random_list))
path = os.path.join('.', "random.txt")
with open(path, 'w', encoding='UTF-8') as f:
    f.write(str(number))
with open(path, 'r', encoding='UTF-8') as f:
    num_str = f.read()
random_list = list(num_str)
line_similar = max((list(g) for k, g in itertools.groupby(random_list)), key=len)
line_similar_number = int(''.join(str(i) for i in line_similar))
print(line_similar_number)